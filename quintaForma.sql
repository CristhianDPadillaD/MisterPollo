DROP DATABASE IF EXISTS Pollo;
CREATE DATABASE IF NOT EXISTS Pollo;
USE Pollo;

CREATE TABLE empleados (
    empleadoID INT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    edad INT NOT NULL,
    documento VARCHAR(20) NOT NULL,
    telefono VARCHAR(15),
    email VARCHAR(100),
    tiempo_completo BOOLEAN NOT NULL,
    anio_contratacion DATE NOT NULL
);
CREATE TABLE salarios (
    salarioID INT AUTO_INCREMENT PRIMARY KEY,
    salario DECIMAL(10, 2) NOT NULL
);
CREATE TABLE puestos (
    puestoID INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL,
    salarioID INT,
    FOREIGN KEY (salarioID) REFERENCES salarios(salarioID)
);
CREATE TABLE sedes (
    sedeID INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL,
    puestoID INT,
    FOREIGN KEY (puestoID) REFERENCES puestos(puestoID)
);




CREATE TABLE horarios (
    horarioID INT AUTO_INCREMENT PRIMARY KEY,
    empleadoID INT,
    horario_entrada TIME NOT NULL,
    horario_salida TIME NOT NULL,
    FOREIGN KEY (empleadoID) REFERENCES empleados(empleadoID)
);


CREATE TABLE sede_puesto_salario (
    id INT AUTO_INCREMENT PRIMARY KEY,
    sedeID INT NOT NULL,
    puestoID INT NOT NULL,
    salarioID INT NOT NULL,
    FOREIGN KEY (sedeID) REFERENCES sedes(sedeID),
    FOREIGN KEY (puestoID) REFERENCES puestos(puestoID),
    FOREIGN KEY (salarioID) REFERENCES salarios(salarioID)
);

CREATE TABLE empleado_horarios (
    id INT AUTO_INCREMENT PRIMARY KEY,
    empleadoID INT NOT NULL,
    horarioID INT NOT NULL,
    FOREIGN KEY (empleadoID) REFERENCES empleados(empleadoID),
    FOREIGN KEY (horarioID) REFERENCES horarios(horarioID)
);

CREATE TABLE empleado_sede_puesto_salario (
    id INT AUTO_INCREMENT PRIMARY KEY,
    sede_puesto_salario_id INT NOT NULL,
    empleado_horarios_id INT NOT NULL,
    FOREIGN KEY (sede_puesto_salario_id) REFERENCES sede_puesto_salario(id),
    FOREIGN KEY (empleado_horarios_id) REFERENCES empleado_horarios(id)
);


